public class questionOne {

	public static void main(String[] args) {

		/// print the sum of all values in an array
		
		int[] a = {2,4,6};
		
		int	sum=0;
		

		for (int j=0;j<a.length;j++) {
			sum += a[j] ;
			System.out.println("Sum now equals: "+sum);
			
		}
		System.out.println();
		System.out.println(sum);

		
		
		
		
		
	}

}
